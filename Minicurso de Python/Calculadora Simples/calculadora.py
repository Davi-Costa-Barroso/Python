def soma():
  valor1 = float(input("Digite valor 1: "))
  valor2 = float(input("Digite valor 2: "))
  soma = valor1 + valor2
  return soma
def subtracao():
  a = float(input("Primeiro numero: "))
  b = float(input("Segundo numero: "))
  subtracao = a - b
  return subtracao
def multiplicacao():
  a = float(input("Primeiro numero: "))
  b = float(input("Segundo numero: "))
  multiplicacao = a * b
  return multiplicacao  
def divisao():
  a = float(input("Digite valor 1: "))
  b = float(input("Digite valor 2: "))
  divisao = a / b
  return divisao
def raiz():
  a = int(input("Digite valor: "))
  raiz = a ** 0.5
  return raiz